.run_report_script <- function(root, args = character()) {
  processx::run(
    command = "./tools/report_components.R",
    args = args,
    wd = root,
    echo = FALSE,
    error_on_status = FALSE
  )
}

testthat::test_that("report_components CLI prints help", {
  root <- withr::local_tempdir()
  .create_fake_repo(root)

  result <- .run_report_script(root, "--help")

  testthat::expect_equal(result$status, 0)
  testthat::expect_match(result$stdout, "Usage: ./tools/report_components.R")
  testthat::expect_match(result$stdout, "--quiet")
})

testthat::test_that("report_components CLI writes manifests and reports", {
  root <- withr::local_tempdir()
  .create_fake_repo(root)

  result <- .run_report_script(root)

  testthat::expect_equal(result$status, 0)
  testthat::expect_match(result$stderr, "Generating reports \\.{2,} Done")
  testthat::expect_true(file.exists(file.path(
    root, "manifests", "component-coverage.yaml"
  )))
  testthat::expect_true(file.exists(file.path(root, "report", "summary.md")))
})
