.write_file <- function(path, lines = "x") {
  dir.create(dirname(path), recursive = TRUE, showWarnings = FALSE)
  writeLines(lines, path)
}

.write_json <- function(path, object) {
  dir.create(dirname(path), recursive = TRUE, showWarnings = FALSE)
  jsonlite::write_json(
    object,
    path = path,
    auto_unbox = TRUE,
    pretty = TRUE,
    null = "null"
  )
}

.copy_report_tool <- function(root) {
  dir.create(
    file.path(root, "tools", "generate"),
    recursive = TRUE,
    showWarnings = FALSE
  )
  file.copy(
    normalizePath(
      file.path("..", "..", "report_components.R"),
      mustWork = TRUE
    ),
    file.path(root, "tools", "report_components.R")
  )
  file.copy(
    normalizePath(file.path("..", "..", "cli_ui.R"), mustWork = TRUE),
    file.path(root, "tools", "cli_ui.R")
  )
  file.copy(
    normalizePath(file.path("..", "..", "integrity.R"), mustWork = TRUE),
    file.path(root, "tools", "integrity.R")
  )

  helper_files <- c(
    "utils.R",
    "policy.R",
    "metadata.R",
    "schema.R",
    "render_utils.R",
    "render_wrappers.R",
    "render_updates.R",
    "render_bindings.R"
  )
  for (file in helper_files) {
    file.copy(
      normalizePath(file.path("..", "..", "generate", file), mustWork = TRUE),
      file.path(root, "tools", "generate", file)
    )
  }

  Sys.chmod(file.path(root, "tools", "report_components.R"), mode = "0755")
}

