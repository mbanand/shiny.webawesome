# Template rendering helpers for the generate stage.
# nolint start: object_usage_linter.

# Escape one string for use inside a double-quoted R literal.
.r_string <- function(value) {
  value <- as.character(value %||% "")
  value <- gsub("\\\\", "\\\\\\\\", value, perl = TRUE)
  value <- gsub("\"", "\\\\\"", value, fixed = TRUE)
  paste0("\"", value, "\"")
}

# Escape one string for use inside a single-quoted JS literal.
.js_string <- function(value) {
  value <- as.character(value %||% "")
  value <- gsub("\\\\", "\\\\\\\\", value, perl = TRUE)
  value <- gsub("'", "\\\\'", value, fixed = TRUE)
  paste0("'", value, "'")
}

# Render one text template with `{{TOKEN}}` placeholders.
.render_template <- function(template_path, values) {
  text <- paste(
    readLines(template_path, warn = FALSE, encoding = "UTF-8"),
    collapse = "\n"
  )

  for (name in names(values)) {
    pattern <- paste0("\\{\\{", name, "\\}\\}")
    text <- gsub(pattern, values[[name]], text, perl = TRUE)
  }

  text
}

# Return the standard generated file header comment.
.generated_header <- function() {
  paste(
    "# Generated by tools/generate_components.R.",
    "Do not edit by hand."
  )
}
